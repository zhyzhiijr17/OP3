

class Program
{
    static void Main()
    {
        // Введення граничного значення
        Console.Write("Введіть граничне число: ");
        int limit = int.Parse(Console.ReadLine());

        int a = 0, b = 1;  // Початкові числа Фібоначчі
        int sum = 0;       // Сума чисел Фібоначчі
        int count = 0;     // Лічильник кількості елементів

        // Цикл, що обчислює числа Фібоначчі, доки вони менші за граничне значення
        while (a < limit)
        {
            sum += a;  // Додавання поточного числа Фібоначчі до суми
            count++;   // Збільшення кількості елементів

            // Обчислення наступного числа Фібоначчі
            int temp = a;
            a = b;
            b = temp + b;
        }

        // Виведення результатів
        Console.WriteLine($"Сума чисел Фібоначчі, менших за {limit}: {sum}");
        Console.WriteLine($"Кількість елементів Фібоначчі: {count}");
    }
}
